
/**
 * Represents a simple calculator.
 */
public final class Calculator {

    /**
     * Sums two integers.
     *
     * @param a the first operand
     * @param b the second operand
     * @return the sum of {@code a} and {@code b}
     */
    public static int sum(int a, int b) {
        return a + b;
    }
}